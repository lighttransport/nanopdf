#!/usr/bin/env python3
"""Generate C accessors for one embedded binary resource.

Modes:
  incbin: writes <base>.h, <base>.c, and <base>.S using assembler .incbin
  array:  writes <base>.h and <base>.c using a C byte array
"""

import argparse
import os
import re


def c_ident(value):
    value = re.sub(r"[^0-9A-Za-z_]", "_", value)
    if value and value[0].isdigit():
        value = "_" + value
    return value


def write_if_changed(path, data):
    old = None
    if os.path.exists(path):
        with open(path, "rb") as f:
            old = f.read()
    if old != data:
        with open(path, "wb") as f:
            f.write(data)


def generate_header(path, guard, api):
    text = f"""/* Auto-generated by scripts/embed_binary_resource.py. */
#ifndef {guard}
#define {guard}

#include <stddef.h>

#ifdef __cplusplus
extern "C" {{
#endif

const unsigned char *{api}_data(void);
size_t {api}_size(void);

#ifdef __cplusplus
}}
#endif

#endif /* {guard} */
"""
    write_if_changed(path, text.encode("utf-8"))


def generate_array_c(path, header_name, api, resource_path):
    with open(resource_path, "rb") as f:
        data = f.read()

    lines = []
    lines.append("/* Auto-generated by scripts/embed_binary_resource.py. */\n")
    lines.append(f'#include "{header_name}"\n\n')
    lines.append(f"static const unsigned char {api}_bytes[] = {{\n")
    for i in range(0, len(data), 12):
        chunk = data[i:i + 12]
        lines.append("    " + ", ".join(f"0x{b:02x}" for b in chunk))
        lines.append(",\n" if i + 12 < len(data) else "\n")
    lines.append("};\n\n")
    lines.append(f"const unsigned char *{api}_data(void)\n{{\n")
    lines.append(f"    return {api}_bytes;\n}}\n\n")
    lines.append(f"size_t {api}_size(void)\n{{\n")
    lines.append(f"    return sizeof({api}_bytes);\n}}\n")
    write_if_changed(path, "".join(lines).encode("utf-8"))


def generate_incbin_c(path, header_name, api, symbol):
    text = f"""/* Auto-generated by scripts/embed_binary_resource.py. */
#include "{header_name}"

extern const unsigned char {symbol}_start[];
extern const unsigned char {symbol}_end[];

const unsigned char *{api}_data(void)
{{
    return {symbol}_start;
}}

size_t {api}_size(void)
{{
    return (size_t)({symbol}_end - {symbol}_start);
}}
"""
    write_if_changed(path, text.encode("utf-8"))


def generate_incbin_s(path, symbol, resource_path):
    inc_path = os.path.abspath(resource_path).replace("\\", "/")
    text = f"""/* Auto-generated by scripts/embed_binary_resource.py. */
#if defined(__linux__) && (defined(__ELF__) || defined(__linux))
    .section .note.GNU-stack,"",@progbits
#endif
#if defined(__APPLE__)
    .section __TEXT,__const
    .globl _{symbol}_start
_{symbol}_start:
    .incbin "{inc_path}"
    .globl _{symbol}_end
_{symbol}_end:
#else
    .section .rodata
    .globl {symbol}_start
    .type  {symbol}_start, @object
    .align 16
{symbol}_start:
    .incbin "{inc_path}"
    .globl {symbol}_end
{symbol}_end:
    .size {symbol}_start, {symbol}_end - {symbol}_start
#endif
"""
    write_if_changed(path, text.encode("utf-8"))


def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--input", required=True)
    parser.add_argument("--out-dir", required=True)
    parser.add_argument("--base", required=True)
    parser.add_argument("--api", required=True)
    parser.add_argument("--mode", choices=("incbin", "array"), required=True)
    args = parser.parse_args()

    if not os.path.isfile(args.input):
        raise SystemExit(f"input not found: {args.input}")

    os.makedirs(args.out_dir, exist_ok=True)
    base = c_ident(args.base)
    api = c_ident(args.api)
    symbol = api
    guard = c_ident(base).upper() + "_H"

    h_path = os.path.join(args.out_dir, base + ".h")
    c_path = os.path.join(args.out_dir, base + ".c")
    s_path = os.path.join(args.out_dir, base + ".S")
    header_name = os.path.basename(h_path)

    generate_header(h_path, guard, api)
    if args.mode == "incbin":
        generate_incbin_c(c_path, header_name, api, symbol)
        generate_incbin_s(s_path, symbol, args.input)
    else:
        generate_array_c(c_path, header_name, api, args.input)
        if os.path.exists(s_path):
            os.remove(s_path)


if __name__ == "__main__":
    main()
